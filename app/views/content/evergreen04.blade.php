            Waiting in line has become an inevitable of the modern human condition. Yearly, Americans spend 37 billion hours waiting in line.
            <br /><br />
            We wait for everything; we wait for the grocer to ring up our purchases. We wait at the airport for our plane to arrive.
            <br /><br />
            We spend valuable time away from the important things in life, like family and our loved ones, in order to withdraw money, to get passports and visas, to enroll in school, to eat in restaurants, to buy tickets, to take rides in the amusement parks, to see doctors and dentists, to buy medicine, and many more.
            <br /><br />
            Different forms of waiting make us anxious and different places evoke different situations where waiting occurs.
            <br /><br />
            For a business to understand where to begin managing their lines, let’s look at a couple of examples:
            <br /><br />
            <h4>Customers get anxious -- engage them</h4>
            A common experience, usually in restaurants and other food establishments, is the anxiety people feel while waiting to be acknowledged.  It isn't uncommon for restaurant staff to approach a customer right as they sit down with a common spiel: "Hello there, here's the menu. I'll be right with you."
            <br /><br />
            There is a fear of being ignored. Letting them know that their presence has been acknowledged calms their feelings as well as sends a powerful message: our service will begin shortly. You've already been queued.
            <br /><br />
            In a hospital, patients entering a busy ER are met by a nurse who records the patient's personal details, complaints and symptoms. From there, the ER administrator can decide whether the patient required the attention of a doctor or if a nurse practitioner can handle the situation.
            <br /><br />
            While a triage system does not cut down on the time it takes to see a medical service provider, the act of engaging the patients if only to record their symptoms makes people waiting in a space where anxiety levels do get high feel like they're one step closer to getting relief.
            <br /><br />
            <h4>Uncertainty makes waiting seem longer</h4>
            Speaking of anxiety, a sure way to get a patron worried is to give a vague answer to the question: "When will I be served?"
            <br /><br />
            People waiting for a service who are told that they will be accommodated within thirty minutes experience an initial annoyance but eventually settle down while waiting.
            <br /><br />
            However, tell a patron that they will be served soon -- without a concrete figure of time -- and the person will be in a constant state of anxiety. It isn't uncommon for patrons to take their business elsewhere given the uncertainty of whether they'll be served in a reasonable amount of time or not.
            <br /><br />
            People who do stay when given a vague estimate before they are served are unable to settle down. They become afraid to depart and come back for fear that they will lose their place in line.
            <br /><br />
            Simply stating how long the wait will take can be a poor way to manage patron's expectations. On a flight, the pilot who repeatedly announces that the plane will land in "a few more minutes" over the intercom adds insult to injury when the wait goes on. Information must be given upfront, and with honesty and transparency.
            <br /><br />
            <h4>Unexplained delays are worse than explained delays</h4>
            In contrast with our example from above, the pilot who announces the reason for the delay (runway congestion, fog bank) are received better if not more warmly. While the announcements and explanations are not enough to absolve the airline or service provider, it is better than giving no explanation at all.
            <br /><br />
            An offshoot benefit to handing out better information is in giving customers the freedom to explore activities they would otherwise not be able to when they are kept in the dark.
            <br /><br />
            For any type of waiting that occurs, a truism that stays constant is this: Unoccupied time moves slower than occupied time.
            <br /><br />
            By explaining the reason for delays, and giving an honest and accurate estimate as to when a service can be delivered, customers who were afraid to depart for fear of losing out on a service are now free to do activities that are important to them.
            <br /><br />
            We all agree –- waiting is terrible. It is something we would do away with if we can.
            <br /><br />
            Study after study shows that businesses who let customers wait are on the precipice of losing business; unhappy customers waiting in line translates to lost sales and goodwill.
            <br /><br />
            But by learning how and when customers experience anxiety in the waiting process, businesses can easily reach out and improve customer relations and business efficiency in meaningful ways.
