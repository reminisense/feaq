            <h3>A Walk Through The Airport</h3>
            <br />
            Airports faced an interesting problem. The number of complaints they were getting in the '90's were increasing. Majority of these complaints came from a single place: the baggage carousel. The riding public did not appreciate having to wait upwards of 10 to 15 minutes for their bags to arrive after they have deplaned.
            <br /><br />
            The problem was simple: Passengers would get off of the plane and reach the baggage carousel area before the ground service crew could retrieve all of the baggage from the plane and deliver it to the carousel area. This meant that passengers were made to wait until all of the baggage could be delivered. It didn't help that the baggage carousel areas were often unadorned spaces that provided little to no entertainment.
            <br /><br />
            The solution was even more elegant: by moving the arrival gate further away from the baggage carousel area, passengers were made to walk the equivalent of 10-15 minutes to reach their luggage -- the exact service time it would take to bring their bags from the plane and onto the metal gurney.
            <br /><br />
            The result was staggering. Because of the small change, passengers complained less as they found that their bags were already waiting for them when they reached the carousel. Airports completed the change by making the passenger's walk from plane to the carousel area a pleasant one; restaurants, shopping kiosks and other scenic if urban spaces dotted the 10 minute trek, occupying the riding public's time and keeping their minds off of the waiting process.
