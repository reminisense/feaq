        <p><h2>Three Commands Explained</h2></p>
        <h3>Call</h3>
        <p>The <b>Call</b> command calls the next available number as denoted in the drop down bar. Calling a number flashes the information on the broadcast screen, telling the customer associated to that number that the terminal is ready to transact with them.</p>
        <p>The <b>Call</b> command works best when calling priority numbers out of sequence. You can select a specific number from the drop down menu, hit <b>Call</b>, and that number will be called out on the broadcast screen.</p>
        <h3>Serve</h3>
        <p>It's close to lunch time and you have a few numbers left in the process field. Using the <b>Serve</b> command on those numbers automatically marks them as done, removing them from the broadcast screen.</p>
        <p>The <b>Serve</b> command is used for when a terminal user wants to conclude a transaction but does not want to begin a new transaction.</p>
        <h3>Next</h3>
        <p>Once a number is called, it appears below the drop down menu in the process field. Selecting <b>Next</b> automatically marks the number as done and immediately calls the next available number as it appears on the drop down menu.</p>
        <p>For businesses that transact priority numbers in sequence, this is the single most important command in the process queue page.</p>
        <p><h2>A fourth option: Dropping a priority number</h2></p>
        <h3>Drop</h3>
        <p>The trash can icon that appears next to the <b>Next</b> command in the process field is called <b>Drop</b> command. Issuing a <b>Drop</b> command is used for no-shows; it is meant for that customers renege or enter the line but decide to leave before being served.</p>
        <p><h2>How It Affects Analytics</h2></p>
        <h3>FeatherQ tracks which commands are issued</h3>
        <p>As part of FeatherQ's <b>Business Analytics</b> feature, FeatherQ analyzes commands issue when processing your lines.</p>
        <p>Using the correct command at the correct time generates important data for the businesses. A high amount of <b>Drop</b> commands issued tells a business owner that they should look into parts of their operations that prevent their customers from completing their transactions.</p>
        <p>Similarly, businesses can track how quickly they can serve customers since they can see how many people are transacting using the <b>Call, Next and Serve</b> commands. These also affect a different metric: the average waiting time. A high number of customers being served in a business operation yields a faster average waiting time, informing business owners that their ship is sailing smoothly.</p>
