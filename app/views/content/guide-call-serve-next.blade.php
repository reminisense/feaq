        <p>Calling out priority numbers using FeatherQ is easy, but we often get asked: "What's the difference between calling a number, hitting next and hitting serve? Does it matter?"</p>
        <p>In a nutshell, correctly using the <b>Call, Serve and Next</b> buttons can help your business keep more accurate statistics:</p>
        <p>
        <ul>
            <li>FeatherQ tracks many metrics on the site to provide <b>valuable business analytics</b>. Using the correct queuing command helps us, help you</li>
            <li>Using the correct command can help your terminal users process queues faster and more efficient</li>
        </ul>
