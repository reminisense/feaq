            <h3>Multiple Lines versus a Single Line: Meet the Serpentine Queue</h3>
            <br />
            MIT Professor Richard Larson is known in many circles of operations management as the father of queuing. Among the many innovations he introduced to the field of queuing science, Larson is best remembered for the serpentine queue.
            <br /><br />
            A serpentine queue is a single-format line, often used in places where multiple terminals accommodate a variety of customers, which acts as a truly equitable line system.
            <br /><br />
            In a serpentine queue, the first customer to arrive at an establishment is always at the head of the line. Whenever a terminal or counter is freed up, that customer heads directly to the open terminal.
            <br /><br />
            When a line does form up, people are served based on how early they arrived at an establishment. The line itself moves quickly and smoothly, because customers are served as soon as a terminal is done with a transaction.
            <br /><br />
            Most businesses however, use a multiple line format to serve customers transacting at different terminals. The result however leads to customer dissatisfaction.
            <br /><br />
            In a multiple line format, customers are more likely to engage in jockeying -- the act of entering a line and then switching to a different one in an effort to reduce waiting time.
            <br /><br />
            Customers who may have arrived first are invariably subjected to seeing the person who just walked into an establishment get served first. As a result, patrons often feel frustrated and associate their frustrations with poor service and management from the business.
            <br /><br />
            <h3>More than line formats:  Perception is Key</h3>
            <br />
            With the help of experts with a clear grasp of queuing theory, businesses now use a single serpentine line to guarantee that the first person who came in will be the first one to be served. This in turn builds goodwill between customer and business, not to mention optimizing operations.
            <br /><br />
            But more than just a simple change in line format, Larson says that the serpentine queue succeeds because it changes the wait in a very creative way -- by changing the perception associated by the person in line.
            <br /><br />
            Larson found that by changing the way a person perceives their wait, a business or establishment can eliminate poor customer feedback, improve productivity and increase on the return business of their patrons.
            <br /><br />
            In this case, customers were getting upset that they weren't being served first despite coming in early because of how the lines were setup.
            <br /><br />
            In the end, paying attention to what ails customers can create a powerful change. As Larson is fond of saying: "You can change a queuing experience into a very positive experience."
