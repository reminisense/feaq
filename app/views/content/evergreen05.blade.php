            In an average lifetime, a person loses a total of almost three years while waiting in line. Despite the fast-paced and instant-service world that modern living has afforded us, one cannot escape lining up to pay the bills, to buy groceries, to gas up, to ride an elevator or to ride a plane.
            <br /><br />
            Waiting in one area compounds the time you spend idling in other areas. Take an extra five to 10 minutes waiting in line at the grocers and suddenly, you're stuck in the middle of rush hour traffic.
            <br /><br />
            This is especially true in a fully urbanized city. According to Texas A&M's annual mobility study in 2011 they concluded that, "congestion caused urban Americans to travel 5.5 billion hours more." That is the equivalent of waiting the equivalent of a full week at work stuck in traffic, every year, per commuter.
            <br /><br />
            While these activities are undoubtedly important, the wasted time spent waiting for these modern necessities keep us away from the things that truly matter: Dinner plans with loved ones cancelled. Tickets to the big game wasted. The opening scene of the summer's biggest blockbuster missed.
            <br /><br />
            We all agree: Waiting is terrible. It is absolutely something we would do away with if we could.
            <br /><br />
            The dislike and inconvenience that an unreasonable wait brings is so universal that it is no surprise that it extends to the world of business in the form of Queue Management.
            <br /><br />
            Study after study shows that businesses who let customers wait are on the precipice of losing business; unhappy customers waiting in line translates to lost sales and goodwill.
            <br /><br />
            Often, the loss of goodwill between customer and business is the most striking blow.
            <br /><br />
            Thankfully, studies in behavioral science have revealed what customers hate most about waiting, giving companies' a rough guide on how to address the problem:
            <br /><br />
            <h4>People hate it when they don't know how long to wait</h4>
            To a person in line, nothing is more nerve-wracking than not knowing when you will be served. Waiting a long time for an important service is fine for most people. It is when they have no information to help guide the rest of their activities afterward that creates an irate mindset.
            <br /><br />
            To ease their troubled nerves, companies invest heavily in monitors, and intercom systems to inform patrons about their waiting status. These include how fast the line is moving, who is getting served next and -- most importantly -- if there are any delays in the operation.
            <br /><br />
            Providing these all-important tidbits of information to customers have a drastic effect. Airports around the world suffer delays all the time, but the riding public generally tolerate their wait inside airport facilities, if not outright enjoy it. This is because airports are abundant in information to help travelers reach their destinations in a timely manner.
            <br /><br />
            <h4>People will not wait for a service or a product if they are bored</h4>
            When people are unstimulated while waiting in line, they are more likely to abandon whatever they are queued for. This is known as Reneging. The customer enters the line but decides to leave before being served.
            <br /><br />
            People are willing to wait more if they are comfortable, engaged or entertained.  Companies have to design spaces where people can wait comfortably. Often, these areas have to provide entertainment. These can range from TV programs on multiple monitors, to the all-important Free Wifi connection.
            <br /><br />
            <h4>People DISLIKE it when somebody behind gets served ahead</h4>
            This simply goes against a person's sense of personal justice. Seeing someone served ahead, when you clearly came in first infuriates even the most level-headed of people. Companies in queueing systems that have priority numbers, ensuring that those who come in first will be first to be served out.
            <br /><br />
            When nobody is jumping the queue, customers have less anxiety to worry about.
            <br /><br />
            Businesses are spending millions to fix this problem and it's easy to see why -- Customers like businesses who recognize the enormous inconvenience they are being subjected to. Eliminating those inconveniences builds goodwill.
            <br /><br />
            But the cost of managing lines does not need to be astronomical. A small team of developers have studied queuing in depth and have come up with a convenient solution. FeatherQ is an online, multiple-notification, free queuing management system, available for everyone that needs to manage their line.
            <br /><br />
            But more than managing lines, FeatherQ aims to make waiting convenient and fun.
            <br /><br />
            FeatherQ places the broadcast monitor and the intercom system into the hands of customers: through their smartphone or tablet. Instead of waiting in a cramped waiting area, uncertain when you can be served, FeatherQ sends timely updates about the status of your wait via SMS or Email.
            <br /><br />
            Users can also see the status of the line through a remote broadcast screen on the FeatherQ website.
            <br /><br />
            This way, customers are free to explore or pursue activities worthwhile to them, eliminating the boredom associated with waiting.
            <br /><br />
            In doing so, customers are at ease that their place in line will not be usurped, regardless if they sit still or wander about. With timely updates in the palm of their hands, FeatherQ lets both businesses and customers know: There's a better way to wait.
