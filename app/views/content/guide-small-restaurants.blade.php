        <h3>Setting Up Your Own Queuing Solution</h3>
        <br />
        FeatherQ's no-installation Line Management software and Remote Broadcast screen allows businesses to set up their own state-of-the-art broadcast screen at a fraction of the cost compared to traditional queuing solutions.
        <br /><br />
        <img src="{{ URL::to('/images/guides/small-restaurant-001.png') }}">
        <br /><br />
        To setup your own FeatherQ queuing solution, you will need the following:
        <ul>
            <li>2x Android 10" tablet PC.</li>
            <li>1x Tablet Wall Mount</li>
            <li>1x Tablet Stand</li>
            <li>QR Code printouts for your Business</li>
        </ul>
        <br /><br />
        <h3>Important! What to Look For in a Tablet PC</h3>
        When looking for a Tablet PC for your FeatherQ software, you want to keep in mind two things: battery efficiency and good heat dissipation.
        <br /><br />
        We will be using these tablets to create your own queuing control panel that fits right next to your POS machine and as a broadcast screen that people can check on to see the status of their wait.
        <br /><br />
        For the queuing control panel, you want a tablet model that has great battery efficiency so that you don’t need to charge your tablet during a busy operation.
        <br /><br />
        For the broadcast panel, you want a tablet that can withstand prolonged hours of use without overheating. We recommend placing the tablet in a well-ventilated spot, preferably close to a wall socket, that’s also visible to your patrons.
        <br /><br />
        <h3>Setting Up Your Broadcast Screen</h3>
        Once you've identified a suitable location to place your broadcast screen, simply install the wall mount and install the tablet.
        <br /><br />
        <img src="{{ URL::to('/images/guides/small-restaurant-002.png') }}">
        <br /><br />
        On your tablet settings, go to Settings>Screen Refresh and set it to Indefinite. This ensures that the tablet will keep showing the same screen and not go into screensaver mode.
        <br /><br />
        Now go to your browser, logon to <a href="http://www.featherq.com">www.FeatherQ.com</a>, and go to the MyBusiness tab. Under Broadcast, select Numbers Only display
        <br /><br />
        <img src="{{ URL::to('/images/guides/small-restaurant-003.png') }}">
        <br /><br />
        Once you have your business' broadcast page displayed, connect the tablet to a power source. You now have a working broadcast monitor!
        <br /><br />
        <img src="{{ URL::to('/images/guides/small-restaurant-004.png') }}">
        <br /><br />
        TIP: We recommend placing your tablet broadcast monitor at eye length. This allows most people to still see updates despite the tablet's smaller frame.
        <br /><br />
        <h3>Setting Up Your Queuing Control Panel</h3>
        To set up your queuing control panel, place the tablet stand near your POS machine. Set your tablet on the stand and open the MyBusiness page.
        <br /><br />
        <img src="{{ URL::to('/images/guides/small-restaurant-005.png') }}">
        <br /><br />
        TIP: Busy operations may need a more dynamic approach. It is best to keep the stand close to the POS machine so that it's always on hand. However, don't be afraid to pick up the tablet and call numbers while moving about! FeatherQ allows your business operations to continue smoothly while you interact with your customers.
        <br /><br />
        <h3>An Added Touch: QR Code Standees</h3>
        If you have good internet access at your establishment, we recommend placing QR Code Standees on tables or on the doorframe to your restaurant.
        <br /><br />
        This way, customers can simply scan the code on their devices and instantly see the Remote Broadcast screen. This is perfect for restos and cafes with multiple floors, eliminating the need for multiple tablet or monitor setups.
        <br /><br />
        And there you have it! A complete, queuing solution setup perfect for your restaurant or cafe!

