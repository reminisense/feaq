        <h3>What is Queuing and Queuing Theory?</h3>
        <p>Simply put, a queue is a line of people or things waiting to be handled, usually in sequential order starting at the beginning or top of the line or sequence.</p>
        <p>In operations management, understanding how a queue works falls under queuing theory. Queuing Theory is defined as, "a mathematical method of analyzing the congestions and delays of waiting in line or queue (Investopedia)."</p>
        <p>It examines every component of waiting. This includes the arrival process, service process, number of servers, number of system places and -- most especially -- you, the customers.</p>
        <p>In the business world, queuing theory is used to provide faster customer service and optimize business logistics.</p>
        <p>So how is this useful? Simply put, a strong understanding of queuing theory can be used by business owners, service managers and front line staff to reduce customer wait times, increase the number of customers that can be served and entice repeat business from patrons.</p>
        <p>Before we continue, here are some terms to get us started on the same page:</p>
        <h3>The Golden Rule of Customers and Lining Up</h3>
        <p>
        <ul>
            <li><b>First In, First Out</b>: The principle stating that the person who has waited in line longest will be served first.</li>
        </ul>
        </p>
        <h3>Types of Lines: The Right Line for the Right Service</h3>
        <p>
        <ul>
            <li><b>Multiple Line/Terminal</b>: A multiple line formation where customers queue up in single lines in front of different terminals. While effective for small volume establishments, a multiple line system encourages jockeying among customers, which in turn lead to disgruntled people waiting in line.</li>
            <li><b>Serpentine Line/Serpentine Queue</b>: A single line formation that feeds into different terminals. The person at the head of a serpentine queue is the first to arrive, and is also the first to be served.</li>
        </ul>
        </p>
        <h3>On Queuing Behavior: How Customers Behave When in Line</h3>
        <p>
        <ul>
            <li><b>Jockeying</b>: When the customer enters one line and then switches to a different one in an effort to reduce waiting time</li>
            <li><b>Reneging</b>: The customer enters the line but decides to leave before being served.</li>
            <li><b>Balking</b>: The customer decides not to enter the waiting line.</li>
            <li><b>Faffing</b>: The time delay when a person gather his things after paying at the checkout. The average is 3.17 seconds, according to Popular Mechanics.</li>
        </ul>
        </p>
