        Studies have repeatedly shown that our perception of time isn't constant. As we grow older, our perception of time is that it is moving faster; this is called the reminiscence effect. As we grow older, we tend to reminisce about the more memorable first events; your first kiss, going to college, getting married and having children.
        <br /><br />
        As time moves forward however, life becomes more routine and more mundane because we've already experienced them. Hence, we create fewer memorable moments and as a result -- time has the illusion of moving faster.
        <br /><br />
        The opposite can also happen to us. Time can move slower for us especially when we're idling or waiting. Time feels like it's moving the slowest when we're waiting in line. Researchers have discovered that on average, a person overestimates the time they've waited in line by as much as 36 percent.
        <br /><br />
        Why does this happen? This might be because idleness or doing nothing allows us to observe our external surroundings. This slows down our sense of time because it slows down all other active processes we might have. When you're waiting in line, you are limited with your activities. Without anything meaningful to do, you can literally watch paint dry. Slowly.
        <br /><br />
        <h3>What Does It Mean For Your Business</h3>
        <br />
        Customers who wait in line are shown to be more restless and bored. To quote William James, "Boredom results from being attentive to the passage of time".
        <br /><br />
        People who wait in line are more likely to feel unstimulated, and if they aren't invested in your business or service, they might opt to leave the queue.
        <br /><br />
        This is called reneging. The customer enters the line but decides to leave before being served.
        <br /><br />
        People become cognizant of situations where they might potentially be bored, that some opt to not queue up at all in places with a lot of people just idly waiting. Balking happens when a customer decides that their time is worth more than the agony of waiting, so they decide not to enter the waiting line at all.
        <br /><br />
        How many times have we decided not to enter a business because we saw that the line or queue was too long? Failing to address the issue about managing your customer's perception of time results in real opportunity costs for many business owners.
        <br /><br />
        Worse, customers who do queue up but get restless as they wait can spell negative press for your operations.
        <br /><br />
        Learning to manage your customer's waiting time is a necessary step to improving your business. Businesses don't need to change the length of time to have a better service, but rather influence the perception of time by controlling whatever external factors that they can. Here are some quick tips to ensure that your waiting customers aren't stuck in time:
        <br /><br />
        <h3>Engage.</h3>
        Restaurants routinely engage customers as they are sitting down on their table. First, they give them a menu to keep them occupied while the servers or waitstaff are attending to other patrons. While waiting for their meals, a maitre d' will often approach a waiting customer to ask them if they would like some appetizers in the form of dinner rolls.
        <br /><br />
        Doing so lets a customer know that they have been acknowledged and that their meals are being attended to. This keeps them from just staring at the wall as each agonizing ticking of the clock reminds them that their food has not yet been served.
        <br /><br />
        <h3>Inform.</h3>
        Any service center worth its salt prides itself for being able to give quick and timely updates to customers waiting for their appliance, gadget or furniture to be serviced. These businesses know that their customers are taking time off of their busy schedules and are looking to them to get the job done.
        <br /><br />
        By providing patrons crucial information such as a reliable estimate on when their transaction will be completed, these businesses can shape public perception because they've placed their best foot forward; it sends the message that they are a business that prides itself for delivering in a timely manner. And if they can't, they'll make sure you will not wait in vain.
        <br /><br />
        <h3>Entertain.</h3>
        Finally, providing good sources of entertainment for people waiting in line at your business can turn even the most backed up of queues into tolerable, if not enjoyable, experiences. Businesses frequently place monitors playing the big game or a fun, popcorn flick at waiting lobbies or reception areas to take their customer's mind off the wait.
        <br /><br />
        Managing a line can be as simple as talking to your customers at the right time. By focusing your efforts in improving business operations by making sure that customers wait comfortably stimulated as they wait, your business can improve goodwill and increase return business. After all, they had such a pleasant time the first time around that they're bound to remember.
